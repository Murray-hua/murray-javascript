function change(type) {
    if (type == "red") {
        document.getElementById("background").href = "./css/red.css";
    }
    else if (type == "blue") {
        document.getElementById("background").href = "./css/blue.css";
    }
    else if (type == "gray") {
            document.getElementById("background").href = "./css/gray.css";
    }
}